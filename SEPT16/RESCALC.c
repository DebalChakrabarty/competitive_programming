#include<stdio.h>
#include<stdlib.h>
#include<string.h>
int main()
{
	int T,N;
	int c[100],type[6],t[100][100],p[100];
	int i,j,points,k,m=0,l,index,count=0,max,flag=0,f,index1;
	scanf("%d",&T);
	while(T--)
	{
		scanf("%d",&N);
		for(i=0;i<N;i++)
		{
			scanf("%d",&c[i]);
			for(j=0;j<c[i];j++)
				scanf("%d",&t[i][j]);
		}
		for(i=0;i<N;i++)
		{
			for(k=0;k<6;k++)
				type[k]=0;
			points=1;
			points=points*c[i];
			for(j=0;j<c[i];j++)
			{
				if(t[i][j]==1)
					type[0]++;
				if(t[i][j]==2)
					type[1]++;
				if(t[i][j]==3)
					type[2]++;
				if(t[i][j]==4)
					type[3]++;
				if(t[i][j]==5)
					type[4]++;
				if(t[i][j]==6)
					type[5]++;
			}
			f=0;
			while(f!=1)
			{
				m=0;
				for(l=0;l<6;l++)
				{
					if(type[l]>=1)
					{
						m++;
						type[l]--;
					}
				}
				if(m<=3)
				{
					f=1;
				}
				else
				{
					if(m==6)
					{
						points+=4;
					}
					else
					{
						if(m==5)
						{
							points+=2;
						}
						else
						{
							if(m==4)
							{
								points+=1;
							}
						}
					}
				}
			}
			p[i]=points;
		}
		max=p[0];
		index=0;
		count=0;
		for(i=0;i<N;i++)
		{
				if(p[i]>max)
				{
					max=p[i];
					index=i;
				}
		}
		for(i=0;i<N;i++)
		{
			if(p[index]==p[i])
				count++;
		}
		if(count>=2)
			printf("tie\n");
		else
		{
			if(index==0)
				printf("chef\n");
			else 
			{
					printf("%d\n",index+1);
			}
		}
	}
} 
